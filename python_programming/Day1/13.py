#Arithmetic operators with parameters and return type
def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a/b
def mod(a,b):
    return a%b
print(add(10,20))
print(sub(10,20))
print(mul(10,20))
print(div(10,20))
print(mod(10,20))